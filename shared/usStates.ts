const STATE_NAME_LOOKUP = Object.fromEntries(
  US_STATE_OPTIONS.map((state) => [state.toLowerCase(), state])
);

export function normalizeUsState(value?: string | null) {
  const trimmed = String(value || '').trim();
  if (!trimmed) return '';

  const abbreviationMatch = STATE_ABBREVIATION_TO_NAME[trimmed.toUpperCase()];
  if (abbreviationMatch) return abbreviationMatch;

  return STATE_NAME_LOOKUP[trimmed.toLowerCase()] || trimmed;
}

export function isUsStateOption(value?: string | null) {
  const normalized = normalizeUsState(value);
  return US_STATE_OPTIONS.includes(normalized as (typeof US_STATE_OPTIONS)[number]);
}